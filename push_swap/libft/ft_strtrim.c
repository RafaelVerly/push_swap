#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*sub;
	size_t	i;
	size_t	j;

	if (!s1)
		return (NULL);
	i = 0;
	while (s1[i] != '\0' && ft_strchr(set, s1[i]))
		i++;
	j = ft_strlen(s1);
	while (j > i && ft_strchr(set, s1[j]))
		j--;
	sub = ft_substr(s1, i, (j - i + 1));
	if (sub == NULL)
		return (NULL);
	return (sub);
}
